package kit.developers.kitar;

/**
 * Класс для настройки положения и ротации 3D модели
 *
 * Используй эти константы чтобы настроить как модель будет выглядеть на фото
 */
public class ModelConfig {

    // ==================== ОСНОВНЫЕ НАСТРОЙКИ ====================

    /**
     * Масштаб модели относительно размера QR-кода
     * 1.0 = модель того же размера что и QR
     * 1.5 = модель в 1.5 раза больше QR
     * 0.5 = модель в 2 раза меньше QR
     */
    public static final float SCALE = 7.2f;

    // ==================== РОТАЦИЯ (в радианах) ====================

    /**
     * Поворот по оси X (наклон вверх/вниз)
     * Положительные значения = наклон вперед
     * Отрицательные значения = наклон назад
     * Диапазон: от -3.14 до 3.14 (от -180° до 180°)
     */
    public static final float ROTATION_X = -0.1f;

    /**
     * Поворот по оси Y (поворот влево/вправо)
     * Положительные значения = поворот вправо
     * Отрицательные значения = поворот влево
     * Диапазон: от -3.14 до 3.14 (от -180° до 180°)
     */
    public static final float ROTATION_Y = 0.1f;

    /**
     * Поворот по оси Z (крен влево/вправо)
     * Положительные значения = крен по часовой
     * Отрицательные значения = крен против часовой
     * Диапазон: от -3.14 до 3.14 (от -180° до 180°)
     */
    public static final float ROTATION_Z = 0.0f;

    // ==================== СМЕЩЕНИЕ ====================

    /**
     * Смещение по горизонтали от центра QR-кода
     * Положительные значения = сдвиг вправо
     * Отрицательные значения = сдвиг влево
     * 1.0 = смещение на ширину QR-кода
     */
    public static final float OFFSET_X = 0.0f;

    /**
     * Смещение по вертикали от центра QR-кода
     * Положительные значения = сдвиг вниз
     * Отрицательные значения = сдвиг вверх
     * 1.0 = смещение на высоту QR-кода
     */
    public static final float OFFSET_Y = 0.0f;

    /**
     * Смещение по глубине (приближение/удаление)
     * Положительные значения = дальше от камеры
     * Отрицательные значения = ближе к камере
     */
    public static final float OFFSET_Z = 0.0f;

    // ==================== ЦВЕТ МОДЕЛИ ====================

    /**
     * Базовый цвет модели в формате RGB (0-255)
     */
    public static final int COLOR_R = 255;  // Красный компонент
    public static final int COLOR_G = 165;  // Зеленый компонент
    public static final int COLOR_B = 0;    // Синий компонент

    /**
     * Прозрачность модели (0-255)
     * 255 = полностью непрозрачная
     * 128 = полупрозрачная
     */
    public static final int ALPHA = 230;

    // ==================== ПРИМЕРЫ НАСТРОЕК ====================

    /*
     * ПРИМЕР 1: Модель стоит прямо, без поворотов
     * ROTATION_X = 0.0f
     * ROTATION_Y = 0.0f
     * ROTATION_Z = 0.0f
     *
     * ПРИМЕР 2: Модель повернута на 45° вправо
     * ROTATION_Y = 0.785f  // 45 градусов в радианах
     *
     * ПРИМЕР 3: Модель наклонена вперед
     * ROTATION_X = 0.5f
     *
     * ПРИМЕР 4: Модель смещена влево от QR
     * OFFSET_X = -0.5f
     *
     * ПРИМЕР 5: Модель в 2 раза больше QR и над ним
     * SCALE = 2.0f
     * OFFSET_Y = -1.0f
     *
     * ПРИМЕР 6: Красная модель
     * COLOR_R = 255
     * COLOR_G = 0
     * COLOR_B = 0
     *
     * ПРИМЕР 7: Полупрозрачная модель
     * ALPHA = 150
     */

    // ==================== КОНВЕРТАЦИЯ ГРАДУСОВ В РАДИАНЫ ====================

    /**
     * Конвертирует градусы в радианы
     * Используй если тебе удобнее работать с градусами
     *
     * Пример: float rotation = ModelConfig.degreesToRadians(45);
     */
    public static float degreesToRadians(float degrees) {
        return degrees * (float) Math.PI / 180f;
    }

    /**
     * Быстрые константы для популярных углов (в радианах)
     */
    public static final float ANGLE_0 = 0.0f;
    public static final float ANGLE_45 = 0.785f;
    public static final float ANGLE_90 = 1.571f;
    public static final float ANGLE_135 = 2.356f;
    public static final float ANGLE_180 = 3.142f;
    public static final float ANGLE_MINUS_45 = -0.785f;
    public static final float ANGLE_MINUS_90 = -1.571f;
}